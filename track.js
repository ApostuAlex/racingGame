function isWallAtTileCoord(trackTileCol, trackTileRow) {
    let trackIndex = trackTileToIndex(trackTileCol, trackTileRow)
    return (trackGrid[trackIndex] == TRACK_WALL)
}


function trackTileToIndex(tileCol, tileRow) {
    return (tileCol + TRACK_COLS * tileRow);
}


function drawTracks() {
    for (let eachCol = 0; eachCol < TRACK_COLS; eachCol++) {
        for (let eachRow = 0; eachRow < TRACK_ROWS; eachRow++) {
            let trackLeftEdgeX = eachCol * TRACK_W;
            let trackTopEdgeY = eachRow * TRACK_H;
            if (isWallAtTileCoord(eachCol, eachRow))
                canvasContext.drawImage(trackPicWall, trackLeftEdgeX, trackTopEdgeY)
            else
                canvasContext.drawImage(trackPicRoad, trackLeftEdgeX, trackTopEdgeY);
        }
    }
}

function checkForTrackAtPixelCoord(pixelX, pixelY) {
    let tileCol = pixelX / TRACK_W;
    let tileRow = pixelY / TRACK_H;

    tileCol = Math.floor(tileCol);
    tileRow = Math.floor(tileRow);




    if (tileCol < 0 || tileCol > TRACK_COLS || tileRow < 0 || tileRow > TRACK_ROWS) {
        return false;
    }
    let trackIndex = trackTileToIndex(tileCol, tileRow);


    return (trackGrid[trackIndex] == TRACK_ROAD);

}

